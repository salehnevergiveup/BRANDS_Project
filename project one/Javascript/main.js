
var r = document.querySelector(':root');
function normal() {
  r.style.setProperty('--yellow', '#ffe500');
  r.style.setProperty('--backgroundc-one', '#324651');
  r.style.setProperty('--backgroundc-two', '#fff');

}
function darkmode() {
  r.style.setProperty('--yellow', '#c8d6e5');
  r.style.setProperty('--backgroundc-one', '#2d3436');
  r.style.setProperty('--backgroundc-two', '#747d8c');
}